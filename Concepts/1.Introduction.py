# PYTHON: A Open Source Programming Language
# Python is a high-level, interpreted, interactive and object-oriented scripting language.
# Python is designed to be highly readable.
# It creates job opportunities like web development,data analyst, data science, machine learning, etc.
# Python is used for:
# web development (server-side),
# software development,
# mathematics,
# system scripting.
# Python is an interpreted language, which means the source code of a Python program is converted into bytecode that is then executed by
# the Python virtual machine.
# Python is dynamically typed, this means that you don't need to state the type of a variable when you declare it.
# Python is an object-oriented language, which means that it supports classes and objects.
# Python is a multi-paradigm language, which means that it supports different programming styles.
# Python is a cross-platform language, which means that it can run on different operating systems like Windows, Mac, Linux, etc.
# Python is a portable language, which means that it can run on different platforms.

