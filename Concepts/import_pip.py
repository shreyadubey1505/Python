# Modules are used to import a module in a program.
# Types of Modules: Built-in Modules, Externally Installed Modules.
# Built-in Modules: math, random, datetime, os, sys, etc.
# Built-in Modules are already installed with Python, no need to install them.
# Externally Installed Modules are written by other developers and are not installed with Python.
# Externally Installed Modules are installed using pip.
# pip is a package manager for Python.It is used to install, uninstall, upgrade, etc. modules.
# Externally Installed Modules: numpy, pandas, matplotlib, etc.
# To install a module, use the command: pip install module_name
# To uninstall a module, use the command: pip uninstall module_name
# To check the version of a module, use the command: pip show module_name
# To check the list of installed modules, use the command: pip list
# To check the list of outdated modules, use the command: pip list --outdated
# To upgrade a module, use the command: pip install --upgrade module_name
# To upgrade all modules, use the command: pip install --upgrade pip
# To check the help of a module, use the command: pip help
# To check the help of a command, use the command: pip help command_name
# To check the help of a module, use the command: pip help module_name

# import is a  keyword used to import a module in a program.
# Type import Modulename and  press enter to import a module.
# Example
import random

print(random.randint(1, 100))
# OUTPUT
# 56
