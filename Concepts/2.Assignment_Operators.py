#  Assigment Operators
#  =, +=, -=, *=, /=, %=, **=, //=
#  = is used to assign a value to a variable.
#  += is used to add a value to a variable.
#  -= is used to subtract a value from a variable.
#  *= is used to multiply a value to a variable.
#  /= is used to divide a value to a variable.
#  %= is used to find the remainder of a value to a variable.
#  **= is used to find the power of a value to a variable.
#  //= is used to find the floor division of a value to a variable.
#  Example
a = 10
b = 20
print(a)
print(b)
a += b
print(a)
a -= b
print(a)
a *= b
print(a)
a /= b
print(a)
a %= b
print(a)
a **= b
print(a)
a //= b
print(a)
print(a+b)
# OUTPUT
# 10
# 20
# 30
# 10
# 200
# 10.0
# 10.0
# 1e+20
# 5e+18
# 30


